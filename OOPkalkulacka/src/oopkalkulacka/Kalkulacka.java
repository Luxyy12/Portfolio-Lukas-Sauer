package oopkalkulacka;

/**
 *
 * @author Luxy
 */
public class Kalkulacka {
    
    
    public float scitanie(float cisloA, float cisloB) {
        return cisloA + cisloB;
    }
    
    public float odcitane(float cisloA, float cisloB) {
        return cisloA - cisloB;
    }
    
    public float nasobenie(float cisloA, float cisloB) {
        return cisloA * cisloB;
    }
    
    public float delenie(float cisloA, float cisloB) {
        return cisloA / cisloB;
    }
    
}
